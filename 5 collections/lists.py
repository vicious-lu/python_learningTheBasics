#define list with names
names = ['Luis', 'Karla', 'Richard', 'Maria'] #can contain different data types

#printing the list
print (names) #this prints a list

#access different index in order
print(names[0])   # -> Luis   
print(names[1])   # -> Karla
#inverse order
print(names[-2])  # -> Richard
print(names[-1])  # -> Maria